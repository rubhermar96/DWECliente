/* USAR LA BASE DE DATOS LOCAL */
CREATE DATABASE  IF NOT EXISTS `dwec_biblioteca`;
USE `dwec_biblioteca`;


/*SE CREA LA TABLA LIBROS SI NO EXISTE*/
DROP TABLE IF EXISTS `libros`;
CREATE TABLE `libros` (
  `Id` int(11) NOT NULL AUTO_INCREMENT,
  `titulo` varchar(255) DEFAULT NULL,
  `autor` varchar(255) DEFAULT NULL,
  `editorial` varchar(255) DEFAULT NULL,
  `paginas` int(11) DEFAULT NULL,
  `precio` int(11) DEFAULT NULL,
  PRIMARY KEY (`Id`)
) ENGINE=InnoDB AUTO_INCREMENT=59 DEFAULT CHARSET=utf8;


/*SE CREA LA TABLA SOCIOS SI NO EXISTE*/
DROP TABLE IF EXISTS `socios`;
CREATE TABLE `socios` (
  `usuario` varchar(20) NOT NULL,
  `clave` char(32) DEFAULT NULL,
  `nombre` varchar(50) DEFAULT NULL,
  `apellidos` varchar(50) DEFAULT NULL,
  `direccion` varchar(100) DEFAULT NULL,
  PRIMARY KEY (`usuario`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


/*SE CREA LA TABLA PRESTAMOS SI NO EXISTE*/
DROP TABLE IF EXISTS `prestamos`;
CREATE TABLE `prestamos` (
  `Id` int(11) NOT NULL,
  `usuario` varchar(20) NOT NULL,
  `fecha_prestamo` Date DEFAULT NULL,
  `fecha_devolucion` Date DEFAULT NULL,
  PRIMARY KEY (`Id`,`usuario`),
  FOREIGN KEY (`Id`) REFERENCES libros(`Id`) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`usuario`) REFERENCES socios(`usuario`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


